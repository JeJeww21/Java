package Java;

public class logika {
    public static void main(String[] args) {
        /*
         * Logika
         * NOT (!)
         * AND (&&)
         * OR (||)
         */

        /*contoh */
        boolean iya= true;
        boolean tidak= false;

        System.out.println(!tidak+"\n");
        System.out.println(iya && tidak);
        System.out.println("\n");
        System.out.println(iya||tidak);

    }
}
