package Java;

public class tes {
    public static void main(String[] args) {
        System.out.println("Hello World");

        /*Tipe data 
         Karakter :

         * String (Menggunakan petik "...")
         * Char (Menggunakan petik '...')
         
         Angka (tanpa petik) :

         * integer (int)
         * float 
         * double
         * double final
         
         Benar/salah :

         * boolean
        */

        /*Contoh */
        String nama = "Roland Jess Pamarta Adi";
        System.out.println(nama);

        char Gender = 'L';
        System.out.println(Gender);

        int angka = 23;
        System.out.println(angka);

        float angka2 = 24;
        System.out.println(angka2);

        double angka3 = 25;
        System.out.println(angka3);

        boolean benar = true;
        Boolean salah = false;
        System.out.println("Apakah sehat = " + benar);

        /*
         * Escape Sequence
         * \t = tab
         * \n = enter
         * \b = backspace
         */
        
    }
}
