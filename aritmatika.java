package Java;

public class aritmatika {
    public static void main(String[] args) {
        /*Pertambahan */
        int tambah1 = 2;
        int tambah2 = 1;
        System.out.println(tambah1 + tambah2);

        /*Pengurangan */
        int kurang1 = 2;
        int kurang2 = 1;
        System.out.println(kurang1 - kurang2);

        /*Perkalian */
        int perkalian1 = 3;
        int perkalian2 = 4;
        System.out.println(perkalian1 * perkalian2);

        /*Pembagian */
        int pembagian1 = 2;
        int pembagian2 = 2;
        System.out.println(pembagian1 / pembagian2);

        /*Sisa bagi */
        int sisabagi1 = 5;
        int sisabagi2 = 2;
        System.out.println(sisabagi1 % sisabagi2);
    }
}
